"""
Este programa calcula a área de um triângulo usando a fórmula (base * altura) / 2.

Ele solicita a base e a altura do usuário e imprime o resultado.
"""

print("Bem vindx à calculadora Python - Área de triângulo")
base = float(input("Digite a base do triângulo: "))
altura = float(input("Digite a altura do triângulo: "))

area = (base * altura) / 2

print(f"A área do triângulo é: {area}")
