"""
Este programa faz um contador simples de 1 a 5 usando um loop while.
Imprime cada número e ao final exibe uma mensagem de término.

Conceitos praticados:
- Laço while
- Variáveis e incremento
- Função print()
"""

contador = 1

while contador <= 5:
    print(contador)
    contador += 1

print("Fim da contagem.")